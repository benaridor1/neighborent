const LocaleContext = createContext<LocaleContextValue | null>(null);

export function LocaleProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<AppLanguage>("he");
  const [currency, setCurrencyState] = useState<AppCurrency>("ILS");
  const [isLocaleModalOpen, setIsLocaleModalOpen] = useState(false);

  useEffect(() => {
    const storedLanguage = window.localStorage.getItem(LANGUAGE_STORAGE_KEY) as AppLanguage | null;
    const storedCurrency = window.localStorage.getItem(CURRENCY_STORAGE_KEY) as AppCurrency | null;
    if (storedLanguage && ["he", "ar", "ru", "en", "fr"].includes(storedLanguage)) {
      setLanguageState(storedLanguage);
    }
    if (storedCurrency && ["ILS", "USD"].includes(storedCurrency)) {
      setCurrencyState(storedCurrency);
    }
  }, []);

  useEffect(() => {
    window.localStorage.setItem(LANGUAGE_STORAGE_KEY, language);
    document.documentElement.lang = language;
    document.documentElement.dir = language === "he" || language === "ar" ? "rtl" : "ltr";
  }, [language]);

  useEffect(() => {
    window.localStorage.setItem(CURRENCY_STORAGE_KEY, currency);
  }, [currency]);

  const value = useMemo<LocaleContextValue>(
    () => ({
      language,
      currency,
      setLanguage: setLanguageState,
      setCurrency: setCurrencyState,
      formatCurrency: (price: number) => {
        try {
          const localeTag = language === "he" ? "he-IL" : language === "ar" ? "ar-IL" : language;
          const value = currency === "USD" ? Math.round(price / 3.6) : price;
          return new Intl.NumberFormat(localeTag, {
            style: "currency",
            currency,
            maximumFractionDigits: 0,
          }).format(value);
        } catch {
          const sym = currency === "USD" ? "$" : "₪";
          const value = currency === "USD" ? Math.round(price / 3.6) : price;
          return `${sym}${value}`;
        }
      },
      t: (key: string) => {
        const pack = TRANSLATIONS[language] ?? TRANSLATIONS.he;
        return pack[key] ?? TRANSLATIONS.en[key] ?? key;
      },
      isLocaleModalOpen,
      openLocaleModal: () => setIsLocaleModalOpen(true),
      closeLocaleModal: () => setIsLocaleModalOpen(false),
    }),
    [currency, isLocaleModalOpen, language],
  );

  return <LocaleContext.Provider value={value}>{children}</LocaleContext.Provider>;
}

export function useLocale() {
  const context = useContext(LocaleContext);
  if (!context) {
    return {
      language: "he" as AppLanguage,
      currency: "ILS" as AppCurrency,
      setLanguage: () => {},
      setCurrency: () => {},
      formatCurrency: (price: number) => `₪${price}`,
      t: (key: string) => TRANSLATIONS.he[key] ?? TRANSLATIONS.en[key] ?? key,
      isLocaleModalOpen: false,
      openLocaleModal: () => {},
      closeLocaleModal: () => {},
    };
  }
  return context;
}
